def generar_cuadrado(lado: int):
    matriz = []
    for i in range(lado):
        fila = []
        for j in range(lado):
            if i == 0 or i == lado - 1 or j == 0 or j == lado - 1:
                fila.append((1,False))
            else:
                fila.append((0,False))
        matriz.append(fila)
    return matriz

def transformar_matriz(matriz):
    nueva_matriz = []
    for fila in matriz:
        nueva_fila = []
        for valor in fila:
            nueva_fila.append((valor, False))
        nueva_matriz.append(nueva_fila)
    return nueva_matriz

